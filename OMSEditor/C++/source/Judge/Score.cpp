#include "../Music/MusicInfo.h"
#include "Score.h"


//*****************************************************************************************************************************
//	静的メンバ変数
//*****************************************************************************************************************************
const int Score::JUDGE_SCORE[3] =
{
	100,	// SUPER
	50,		// GREAT
	10		// GOOD
};

//*****************************************************************************************************************************
//	初期化・解放
//*****************************************************************************************************************************
//------------------------------------------------------
//	初期化
//------------------------------------------------------
Score::Score()
{
	score = 0;
}

//*****************************************************************************************************************************
//		スコア追加
//*****************************************************************************************************************************
void Score::Add(JUDGE_TYPE type)
{
	score += JUDGE_SCORE[(int)type];
}