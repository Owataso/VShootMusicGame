// カメラクラス
class Camera
{
public:
	//------------------------------------------------------
	//	初期化・解放
	//------------------------------------------------------
	Camera();
	~Camera();

	//------------------------------------------------------
	//	更新
	//------------------------------------------------------
	void Update();
	void Activate();

	//------------------------------------------------------
	//	セッター&ゲッター
	//------------------------------------------------------
	void SetTarget(const Vector3 &target) { m_NextTarget = target; }
	Vector3 GetPos() { return m_pos; }

	void UpWheelEvent()
	{
		m_dist = max(m_dist - 8.0f, 8.0f);
	}

	void DownWheelEvent()
	{
		m_dist += 8.0f;
	}

private:

	//------------------------------------------------------
	//	基本パラメータ
	//------------------------------------------------------
	Vector3	m_pos;			// 位置
	Vector3	m_target;		// 注視点
	Vector3 m_NextTarget;	// 次回の注視点
	Vector3 m_angle;		// カメラアングル

	float m_dist;		//	カメラの距離

	void TargetAngleUpdate();
};